
$('#tori').hover(function () {
    $(this).mouseenter(function () {


   $(this).addClass("animated");
    $(this).addClass("shake");
    $(this).addClass("infinite");
    });
    $(this).mouseleave(function () {
        $(this).removeClass("animated");
        $(this).removeClass("shake");
        $(this).removeClass("infinite");
    });
});
